# ── Hex pair for entry_cost tests ─────────────────────────────────────────────
# from_hex A0101 (section=A, col=1 odd), to_hex A0102 (south neighbor).
# Odd-column S-direction delta = (0, +1): row 1 → row 2 = A0102. ✓

_FROM = "A0101"
_TO   = "A0102"
_DIR  = "S"   # forward direction from _FROM → _TO


def _make_pair(to_terrain: Terrain, hexside: HexsideFeature) -> dict:
    """Construct a minimal {hex_id → Hex} dict for one entry_cost call."""
    from_hex = Hex.from_id(_FROM)
    from_hex.terrain = Terrain.DESERT   # source terrain doesn't affect entry cost

    to_hex = Hex.from_id(_TO)
    to_hex.terrain = to_terrain

    if hexside != HexsideFeature.NONE:
        from_hex.hexsides[_DIR] = hexside   # hexside on the forward side

    return {_FROM: from_hex, _TO: to_hex}


def _unit(motorized: bool) -> Unit:
    return Unit(
        id="test",
        name="Test",
        nationality=Nationality.BRITISH,
        side=Side.COMMONWEALTH,
        type=UnitType.INFANTRY,
        size=UnitSize.BATTALION,
        motorized=motorized,
    )


# ── 8.37 entry_cost parametrized tests ────────────────────────────────────────

_MOVEMENT_CASES = load_fixture("movement_costs_8_37")


@pytest.mark.parametrize(
    "case", _MOVEMENT_CASES,
    ids=[case_id(c) for c in _MOVEMENT_CASES],
)
def test_movement_cost(case, tec):
    inp = case["inputs"]
    exp = case["expected"]
    expected_cost = exp["cp_cost"]

    # Oracle could not read the TEC cell from OCR → need physical rulebook
    if expected_cost == "see_tec":
        pytest.skip("TEC cell not recoverable from OCR — physical rulebook required")

    # Oracle flagged a rule ambiguity — expected may differ from implementation
    if "ambiguity" in exp:
        pytest.xfail(reason=f"rule ambiguity: {exp['ambiguity'][:100]}")

    terrain_name = inp["to_terrain"]
    hexside_name = inp["hexside_feature"]
    motorized    = inp["motorized"]
    weather      = inp.get("weather", "clear")

    terrain = _FIXTURE_TERRAIN.get(terrain_name)
    if terrain is None:
        pytest.skip(f"terrain {terrain_name!r} not in test map")

    hexside = _FIXTURE_HEXSIDE.get(hexside_name)
    if hexside is None:
        pytest.skip(f"hexside {hexside_name!r} not in test map")

    hexes = _make_pair(terrain, hexside)
    hmap  = HexMap(hexes, tec)
    unit  = _unit(motorized)

    result = hmap.entry_cost(unit, _FROM, _TO, weather=weather)

    if expected_cost == "P":
        assert result == "P", (
            f"Expected prohibited (P) for terrain={terrain_name}, "
            f"hexside={hexside_name}, motorized={motorized}; got {result!r}"
        )
    else:
        assert isinstance(result, float), (
            f"Expected float cost, got {result!r}"
        )
        assert result == pytest.approx(expected_cost), (
            f"entry_cost mismatch: terrain={terrain_name}, hexside={hexside_name}, "
            f"motorized={motorized} → got {result}, expected {expected_cost}"
        )


