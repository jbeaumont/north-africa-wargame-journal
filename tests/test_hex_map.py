# ── 10.11/10.21 ZOC parametrized tests ───────────────────────────────────────

_ZOC_CASES = load_fixture("zoc_10_11_10_21")


@pytest.mark.parametrize(
    "case", _ZOC_CASES,
    ids=[case_id(c) for c in _ZOC_CASES],
)
def test_zoc_projection_and_extension(case):
    """
    For each fixture case verify:
    (a) projects_zoc — does the unit/hex meet the rule 10.11 ZOC eligibility?
    (b) zoc_extends_through_hexside — is the crossing hexside a ZOC blocker?

    For 10.21c (entry-prohibition) cases the hexside is "None" but the expected
    extension is False due to target hex impassability; those are covered in the
    separate integration test test_zoc_10_21c_salt_marsh.
    """
    inp = case["inputs"]
    exp = case["expected"]

    unit_type = _FIXTURE_UNIT_TYPE.get(inp["unit_type"], UnitType.INFANTRY)
    unit_size = _FIXTURE_UNIT_SIZE.get(inp["unit_size"], UnitSize.BATTALION)
    cohesion  = inp["unit_cohesion"]
    hex_total = inp["hex_total_sp"]
    crossing  = _FIXTURE_HEXSIDE.get(inp["crossing_hexside"], HexsideFeature.NONE)

    u = Unit(
        id="test-zoc", name="ZOC test",
        nationality=Nationality.BRITISH, side=Side.COMMONWEALTH,
        type=unit_type, size=unit_size, motorized=True,
        cohesion=cohesion, hex_id=_FROM,
    )

    sp = HexMap.unit_stacking_points(u)

    # Rule 10.11 eligibility:
    is_never_type       = u.type in _ZOC_NEVER_TYPES
    cohesion_suppress   = cohesion <= -26
    projects_zoc_calc   = (
        not is_never_type
        and not cohesion_suppress
        and (sp > 1 or hex_total > 1)
    )

    assert projects_zoc_calc == exp["projects_zoc"], (
        f"projects_zoc: got {projects_zoc_calc} "
        f"(type={inp['unit_type']}, size={inp['unit_size']}, sp={sp}, "
        f"hex_total_sp={hex_total}, cohesion={cohesion}), "
        f"expected {exp['projects_zoc']}"
    )

    # Rule 10.21 hexside blocking check (only meaningful when a hexside is specified)
    if crossing != HexsideFeature.NONE and exp["projects_zoc"]:
        blocked       = crossing in _ZOC_BLOCKED_HEXSIDES
        should_extend = exp["zoc_extends_through_hexside"]
        assert (not blocked) == should_extend, (
            f"zoc_extends_through_hexside: hexside={inp['crossing_hexside']!r} "
            f"blocked={blocked}, expected extends={should_extend}"
        )


# ── 10.21c integration: ZOC blocked by entry prohibition ─────────────────────

def test_zoc_10_21c_salt_marsh(tec):
    """
    Tank division at A0101 cannot project ZOC into Salt Marsh A0102 (no track/road).

    Rule 10.21c: ZOC does not extend into a hex that the projecting unit could
    not enter from its present location.  The rulebook's own example is a tank
    battalion and a Salt Marsh.
    """
    from_hex = Hex.from_id(_FROM)
    from_hex.terrain = Terrain.DESERT

    to_hex = Hex.from_id(_TO)
    to_hex.terrain = Terrain.SALT_MARSH  # motorized prohibited without track

    hmap = HexMap({_FROM: from_hex, _TO: to_hex}, tec)

    tank_div = Unit(
        id="tank-div", name="Tank Division",
        nationality=Nationality.BRITISH, side=Side.COMMONWEALTH,
        type=UnitType.ARMOR, size=UnitSize.DIVISION, motorized=True,
        hex_id=_FROM,
    )
    zoc = hmap.zoc_hexes(Side.COMMONWEALTH, [tank_div])
    assert _TO not in zoc, (
        "Tank division must NOT project ZOC into Salt Marsh without track/road "
        "(rule 10.21c)"
    )
